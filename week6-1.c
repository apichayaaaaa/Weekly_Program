//รับข้อมูล String 1 ชุด แล้วแสดงผลลัพธ์โดยเปลี่ยนตัวอักษรตัวพิมพ์เล็กให้กลายเป็นตัวอักษรตัวพิมพ์ใหญ่ และตัวอักษรตัวพิมพ์ใหญ่กลายเป็นตัวอักษรตัวพิมพ์เล็ก(วิธี1)
 #include <stdio.h>
int main()
{
  char *p, c[50];
  p = c;
  printf("Enter your input: ");
  scanf("%s", c);
  while (*p != '\0')
  {
    if (*p >= 'A' && *p <= 'Z')
    {
      printf("%c", *p + 32);
    }
    else if (*p >= 'a' && *p <= 'z')
    {
      printf("%c", *p - 32);
    }
    p++;
  }
  return 0;
}